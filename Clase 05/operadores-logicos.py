#operador "and" => devuelve True si y solo si AMBAS condiciones son correctas
print(True and True)# = True
print(False and True)# = False
print(False and False)# = False
print(True and False)# = False

#operador "or" => devuelve True si una o las dos condiciones son correctas
print(True or True)# = True
print(True or False)# = True
print(False or False)# = False
print(False or True)# = True

#operador "not" => invierte el resultado de la comparacion
print(not True) # = False
print(not False)# = True

print(1 == 1 and 2 == 2)# = True 
print(1 == 1 or 1 == 2)# = True 
print(not 1 == 1)# = False 


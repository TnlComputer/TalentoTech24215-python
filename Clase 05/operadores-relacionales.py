#operadores relacionales (para comparar), devuelven True o False
print(2 == 2) #True / compara si ambos valores son iguales
print(2 != 2) #False / compara si ambos valores son distintos
print(2 > 3) #False / compara si el valor de la izquierda es mayor que el de la derecha
print(2 < 3) #True / compara si el valor de la izquierda es menor que el de al derecha
print(2 >= 2) #True / compara si el valor de la izquierda es mayor o igual al de la derecha
print(2 <= 3) #True / compara si el valor de la izquierda es menor o igual al de la derecha

#comparando strings
print("pera" < "manzana") #False / compara el numero de los codigos ASCII de los caracteres
print("Manzana" > "manzana") #False 
